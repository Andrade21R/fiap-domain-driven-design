package br.com.fiap.encapsulamento.model;

public class Pastel {

	private String sabor;

	private boolean pronto;

	public String getSabor() {
		return sabor;
	}

	public void setSabor(String sabor) {
		this.sabor = sabor;
	}

	public boolean isPronto() {
		return pronto;
	}

	public void setPronto(boolean pronto) {
		this.pronto = pronto;
	}
}
